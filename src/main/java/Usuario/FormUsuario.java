    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if(cAction==0){
            try {
                dtoUsuario.newUsuario(new Usuario(
                        0,
                        txtNombre.getText(),
                        txtApellido.getText(),
                        txtClave.getText(),
                        txtUsuario.getText(),
                        cbRol.getSelectedIndex()-1,
                        null,"" , ""));
                JOptionPane.showMessageDialog(rootPane, "Usuario creado exitosamente","Exito",INFORMATION_MESSAGE);
            } catch (SQLException ex) {
                Logger.getLogger(FormUsuario.class.getName()).log(Level.SEVERE, null, ex);
                System.out.println(ex);
            }
        }else{
            try {
                dtoUsuario.updateUsuario(new Usuario(
                        cUser.getId(),
                        txtNombre.getText(),
                        txtApellido.getText(),
                        txtClave.getText(),
                        txtUsuario.getText(),
                        cbRol.getSelectedIndex()-1,
                        "null","" , ""));
                JOptionPane.showMessageDialog(rootPane, "Usuario actualizado exitosamente","Exito",INFORMATION_MESSAGE);
            } catch (SQLException ex) {
                System.out.println(ex);
                Logger.getLogger(FormUsuario.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        try {
            setClosed(true);
        } catch (PropertyVetoException ex) {
            Logger.getLogger(FormUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


